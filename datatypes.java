public class datatypes {
    public static void main(String[] args) {
        byte b =127; //-128 to 127
        short s=32767;//-32,768 to 32,767
        int a=2147483647; //-2,147,483,648 to 2,147,483,647
        long l=994857350;//-9,223,372,036,854,775,808 to 9,223,372,036,854,775,807
        float f= 4300.000f;//fractional numbers from 3.4e−038 to 3.4e+038.
        boolean c=true; //true and false
        char ch='q';
        double d= 3.1415989848943784378454234432481d; //fractional numbers from 1.7e−308 to 1.7e+308. 
        System.out.println(b+" "+s+" "+a+" "+l+" "+f+" "+c+" "+ch+" "+d);
       

    }
    
}
